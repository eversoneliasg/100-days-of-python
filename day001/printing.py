print("Hello, World!")
# Python does not differentiate between single and double quotes when indicating strings.
print('Hello, single quoted World!')
# Creating new lines without duplicating commands with backslash and n
print("Bye, old World!\nHello, new World!")
# We can also concatenate strings
print("Hello, " + "Everson!")
# Entering data with input("A prompt for the user")
# The program will step inside the print function, to what has to be printed
# It sees the input function, executes it. The input is replaced by the typed text
print("Hello, " + input("What is your name? "))
# Calculating user's name length.
print(len(input("What is your name? ")))
