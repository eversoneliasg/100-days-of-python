# Functions
# We have dealt with built-in functions before
# Functions help us avoid code repetition


print("Hello")
num_char = len("Hello")
print(num_char)

# How do we create our own functions?
# We start with the def keyword
# We give a name to the function
# At the end, ():
# For last, the body of the function.


def my_function():
    print("Hello")
    print("Bye")

# Now, we can call/trigger the function.


my_function()
